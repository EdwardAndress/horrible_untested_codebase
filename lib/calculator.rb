class Calculator
  def add(number1, number2)
    number1 + number2
  end

  def subtract(number1, number2)
    number1 - number2
  end

  def divide(dividend, divisor)
    dividend / divisor
  end

  def multiply(number1, number2)
    number1 * number2
  end

  def square(number)
    number * number
  end

  def cube(number)
    number * number * number
  end

  def square_root(number)
    Math.sqrt(number)
  end
end
